char *ft_strncpy(char *dest, char *src, unsigned int n)
{
    // -2,147,483,648 <= int <= 2,147,483,647
    // 0 <= unsigned int <= 4,294,967,295
    unsigned int i;

    i = 0;
    while (src[i] != '\0' && i < n)
    {
        dest[i] = src[i];
        i++;
    }

    while (i < n)
    {
        dest[i] = '\0';
        i++;
    }

    return (dest);
}
